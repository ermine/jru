let rec zip acc = function
  | [], [] -> List.rev acc
  | [], zs -> List.rev (List.fold_left (fun acc x -> x :: acc) acc zs)
  | xs, [] -> List.rev (List.fold_left (fun acc x -> x :: acc) acc xs)
  | x::xs, z::zs ->
    if x.(0) = z.(0) then (
      if x.(1) = z.(1) then (
        x.(2) <- x.(2) lor z.(2);
        zip  (x :: acc) (xs, zs)
      ) else if x.(1) < z.(1) then (
        x.(2) <- x.(2) lor z.(2);
        z.(0) <- x.(1);
        zip (x :: acc)  (xs, z::zs)
      ) else
          let part1 = [| x.(0); z.(1); x.(2) lor z.(2) |] in
          let part2 = [| z.(1); x.(1); x.(2) |] in
            zip (part1::acc) (part2::xs, zs)
    ) else if x.(0) < z.(0) then (
      if x.(1) <= z.(0) then
        zip (x::acc) (xs, z::zs)
      else
        let part1 = [| x.(0); z.(0); x.(2) |] in
        let part2 = [| z.(0); x.(1); x.(2) |] in
          zip (part1::acc) (part2::xs, z::zs)
    ) else (
      if x.(0) >= z.(1) then
        zip (z::acc) (x::xs,zs)
      else
        let part = [| z.(0); x.(0); z.(2) |] in
          z.(0) <- x.(0);
          zip (part::acc) (x::xs, z::zs)
