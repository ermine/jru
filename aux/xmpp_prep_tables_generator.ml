let rec collect_b2 mcf blocks x1 x2 delta = function
  | [] ->
    List.rev mcf, List.rev ([|x1; x2+1; delta lsl b_shift |] :: blocks)
  | (rune, runes) :: zs ->
    match runes with
      | [d] ->
        if x2 + 1 = rune && d - rune = delta then
          collect_b2 mcf blocks x1 rune delta zs
        else
          let newdelta = d - rune in
            collect_b2 mcf
              ([|x1; x2+1; delta lsl b_shift |] :: blocks) 
              rune rune newdelta zs
      | _ ->
        zcollect_b2 (runes :: mcf)
          ([|rune; rune+1; (List.length mcf) lsl b_shift lor b_mc |] ::
              [|x1; x2+1; delta lsl b_shift |] :: blocks) zs
and zcollect_b2 mcf blocks = function
  | [] -> List.rev mcf, List.rev blocks
  | (rune, runes) :: xs ->
    match runes with
      | [d] -> collect_b2 mcf blocks rune rune (d - rune) xs
      | _ ->
        zcollect_b2 (runes :: mcf)
          ([|rune; rune+1;
             (List.length mcf) lsl b_shift lor b_mc|] :: blocks) xs
    
let prepare_prohibits () =
  let nameprep =
    List.map (fun x -> (x, b_nameprep_prohibit))
      (c12 @ c22 @ c3 @ c4 @ c5 @c6 @ c7 @ c8 @ c9) in
  let resourceprep =
    List.map (fun x -> (x, b_resourceprep_prohibit))
      (c12 @ c21 @ c22 @ c3 @ c4 @ c5 @c6 @ c7 @ c8 @ c9) in
  let nodeprep =
    List.map (fun x -> (x, b_nodeprep_prohibit))
      (c11 @ c12 @ c21 @ c22 @ c3 @ c4 @ c5 @ c6 @ c7 @ c8 @ c9 @
         (List.map (fun x -> [x]) xmpp_prohibit))
  in
  let prohibits =
    List.fold_left (fun acc -> function
      | [x], f -> (x, f) :: acc
      | [x1; x2], f ->
        let rec add acc i =
          if i <= x2 then
            add ((i, f) :: acc) (succ i)
          else
            acc
        in
          add acc x1
      | _ -> acc
    ) [] (nameprep @ resourceprep @ nodeprep) in
  let prohibit_list = List.fast_sort compare prohibits in
  let rec reduce acc prev = function
    | [] -> List.rev (prev :: acc)
    | x :: xs ->
      if fst prev = fst x then
        reduce acc (fst x, (snd prev) lor (snd x)) xs
      else
        reduce (prev::acc) x xs
  in
  let reduced = reduce [] (List.hd prohibit_list) (List.tl prohibit_list) in
  let rec collect blocks x1 x2 f = function
    | [] -> List.rev ([|x1; x2+1; f|] :: blocks)
    | (z, f') :: zs ->
      if x2 + 1 = z && f = f' then
        collect blocks x1 z f zs
      else
        collect ([|x1; x2+1; f |] :: blocks) z z f' zs
  in
  let prohibits =
    match reduced with
      | [] -> []
      | (x1, f) :: xs -> collect [] x1 x1 f xs
  in
    prohibits

